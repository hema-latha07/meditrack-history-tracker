		<h4>Heart Attack  (Myocardial Infarction)</h4>
		<div>
		<p id="symchk">-Symptoms</p>
		<p id="heart"> - Chest pain or discomfort (often described as a squeezing or tight sensation)<br>
            - Pain radiating to the arms, jaw, neck, back, or stomach<br>
            - Shortness of breath<br>
            - Nausea or vomiting<br>
            - Cold sweat<br>
            - Dizziness or lightheadedness<br>
            - Fatigue<br>
            - Why it's dangerous: A heart attack occurs when the blood supply to part of the heart <br>
			muscle is blocked, leading to heart muscle damage or death. Immediate treatment<br>
			is critical to minimize damage.<p>
