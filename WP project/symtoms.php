	   		<h4>Cancer</h4>
		    <p id="symchk">-Symptoms(varies by type of cancer,but common signs include)</p>
			
            <p id="cancer">- Unexplained weight loss<br>
                           - Persistent pain (e.g., headaches, back pain)<br>
                           - Unusual lumps or swelling<br>
                           - Chronic fatigue<br>
                           - Changes in skin (e.g., new moles, changes in existing moles)<br>
                           - Persistent cough or hoarseness<br>
                           - Blood in stool, urine, or coughing up blood<br>
                           - Difficulty swallowing<br>
                          - Why it's dangerous: Cancer refers to a group of diseases where cells grow
						  uncontrollably <br>
						  and spread to other parts of the body. Early detection can significantly <br>
						  improve survival rates.</p>
      </div><br><br><br>
	  <div>
	       <h4>HIV/AIDS</h4>
	       <p id="symchk">-Symptoms(initial infection may be mild but can progress to severe symptoms)</p>
		   <p id="hiv"> - Fever, fatigue, and night sweats<br>
                        - Swollen lymph nodes<br>
                        - Unexplained weight loss
                        - Frequent infections (e.g., fungal, bacterial, or viral infections)<br>
                        - Skin rashes<br>
                        - Diarrhea lasting for weeks<br>
                        - Why it's dangerous: HIV (Human Immunodeficiency Virus) attacks the immune system,<br>
						weakening the body’s ability to fight infections. Without treatment, it can lead to AIDS<br>
						(Acquired Immunodeficiency Syndrome), where the body<br> becomes vulnerable to deadly 
						infections and cancers.</p>
