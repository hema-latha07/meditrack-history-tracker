	  	  <p id="heading" font color="brown">Exercise Regularly</p>
          <p>Aim for at least 150 minutes of<br> moderate-intensity exercise or 75 
   minutes<br> of vigorous-intensity exercise per week,<br> along with muscle-strengthening<br>
   activities twice a week. Regular physical<br> activity reduces the risk of many chronic diseases<br>
   and promotes overall health.</p>
