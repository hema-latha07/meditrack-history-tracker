	  	  <p id="heading" font color="brown">Get Enough Sleep</p>
          <p>Aim for 7-9 hours of quality sleep<br> per night. 
   Sleep is crucial for immune function,<br> memory, mood regulation, and overall health.<br>
   Establish a consistent sleep routine,<br> limit screen time before bed,<br> 
   and create a peaceful sleep environment.</p>
