	  <p id="heading" font color="brown">Eat A Balanced Diet</p>
      <p> Eat a variety of foods that provide all<br>essential nutrients.
   Focus on whole grains, <br>fruits, vegetables, lean proteins, and healthy fats.<br>
   Limiting processed foods, added sugars,<br> and unhealthy fats can help prevent chronic diseases 
   like <br>heart disease, diabetes, and obesity.</p>
